  /// 获取指定键的文字（使用当前语言）
  static String get(String key) {
    return _strings[currentLanguage]?[key] ??
        _strings['en']?[key] ??
        _strings['zh']?[key] ??
        key; // 如果都找不到，返回键名本身
  }

  /// 获取指定语言和键的文字
  static String getWithLanguage(String language, String key) {
    return _strings[language]?[key] ??
        _strings['en']?[key] ??
        _strings['zh']?[key] ??
        key;
  }

  /// 设置当前语言
  static void setLanguage(String language) {
    if (_strings.containsKey(language)) {
      currentLanguage = language;
    }
  }

  /// 获取当前语言
  static String getLanguage() {
    return currentLanguage;
  }

  /// 获取所有支持的语言列表
  static List<String> getSupportedLanguages() {
    return _strings.keys.toList();
